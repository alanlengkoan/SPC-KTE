<script src="https://cdnjs.cloudflare.com/ajax/libs/parsley.js/2.9.2/parsley.min.js"></script>

<script>
    // untuk tambah & ubah data
    var untukTambahDanUbahData = function() {
        $(document).on('submit', '#form-add-upd', function(e) {
            e.preventDefault();
            $('#inptotalbayar').attr('required', 'required');
            $('#inpbuktibayar').attr('required', 'required');

            if ($('#form-add-upd').parsley().isValid() == true) {
                $.ajax({
                    method: $(this).attr('method'),
                    url: $(this).attr('action'),
                    data: new FormData(this),
                    contentType: false,
                    processData: false,
                    cache: false,
                    dataType: 'json',
                    beforeSend: function() {
                        $('#pay').attr('disabled', 'disabled');
                        $('#pay').html('<i class="fa fa-spinner"></i>&nbsp;Menunggu...');
                    },
                    success: function(response) {
                        swal({
                            title: response.title,
                            text: response.text,
                            icon: response.type,
                            button: response.button,
                        }).then((value) => {
                            window.location = '<?= distribusi_url() ?>pembelian'
                        });
                        $('#pay').removeAttr('disabled');
                        $('#pay').html('<i class="fa fa-credit-card"></i>&nbsp;Simpan');
                    },
                });
            }
        });
    }();
</script>